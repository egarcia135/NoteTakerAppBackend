package com.notefinder.models;

public class EnrolledAdmin {
	
	private String enrolledFirstName;
	private String enrolledLastName;
	private int enrolledStudentID;
	
	
	public String getEnrolledFirstName() {
		return enrolledFirstName;
	}
	public void setEnrolledFirstName(String enrolledFirstName) {
		this.enrolledFirstName = enrolledFirstName;
	}
	public String getEnrolledLastName() {
		return enrolledLastName;
	}
	public void setEnrolledLastName(String enrolledLastName) {
		this.enrolledLastName = enrolledLastName;
	}
	public int getEnrolledStudentID() {
		return enrolledStudentID;
	}
	public void setEnrolledStudentID(int enrolledStudentID) {
		this.enrolledStudentID = enrolledStudentID;
	}

}
